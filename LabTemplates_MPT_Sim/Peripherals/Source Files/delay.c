/*****************************************************************************
 ** Function name:               delay1ms
 **
 ** descriptions:                Delay by executing a given number of NOPs.
 **                              
 **
 ** parameters:                  number of NOP instructions
 ** returned value:              None
 **
 *****************************************************************************/
void delay1ms(void)
{
  volatile uint32_t i=0;

  for (i = 0; i < MS_1; i++)
    ;
}

//
/*****************************************************************************
 ** Function name:               delayXms
 **
 ** Descriptions:                Delay execution for specified time.
 **
 ** Parameters:                  delay in milliseconds
 ** Returned value:              None
 **
 *****************************************************************************/
void delayXms (uint32_t ms)
{
  volatile uint32_t i;

  for (i = 0; i < ms; i++)
    delay1ms();
}
